package back;

public class BackEnd {

	public BackEnd() {
		
	}

	public long plus(long num1, long num2) {
		return num1 + num2;
	}
	
	public long minus(long num1, long num2) {
		return num1 - num2;
	}
	
	public long multiple(long num1, long num2) {
		return num1 * num2;
	}
	
	public long divide(long num1, long num2) {
		return num1 / num2;
	}
}